package br.edu.ifsul.bcc.lpoo.cv.model;

/**
 *
 * @author Poglia
 */
public class Fornecedor {
    private String cnpj;
    private String ie;
    
    public Fornecedor(){
        
    }

    /**
     * @return the cnpj
     */
    public String getCnpj() {
        return cnpj;
    }

    /**
     * @param cnpj the cnpj to set
     */
    public void setCnpj(String cnpj) {
        this.cnpj = cnpj;
    }

    /**
     * @return the ie
     */
    public String getIe() {
        return ie;
    }

    /**
     * @param ie the ie to set
     */
    public void setIe(String ie) {
        this.ie = ie;
    }
}
